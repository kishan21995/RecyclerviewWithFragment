package com.app.recyclerviewwithfragment.model;

public class Model {

    int image;
    String name;

    //Constructor
    public Model(int image, String name) {
        this.image = image;
        this.name = name;
    }

    //Getter and Setter
    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}










